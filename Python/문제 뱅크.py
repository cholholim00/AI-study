# 7.문자열 s가 주어질 때 'python'이라는 단어가 포함되어 있으면 True, 아니면 False를 출력하라.
print('python' in s) # in → 포함 여부 확인.

# 8.문자열 "hello world"에서 공백을 제거한 문자열을 출력하라.
