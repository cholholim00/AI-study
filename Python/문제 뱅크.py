# 9.문자열 s가 주어질 때 문자열이 회문인지 True / False로 출력하라.
print(s == s[::-1]) # 원본과 뒤집은 문자열 비교.

# 10.문자열 "abc123"에서 숫자만 추출해서 출력하라.
print(''.join(c for c in s if c.isdigit())) # isdigit() → 숫자 판별.

